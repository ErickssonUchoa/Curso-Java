package exercises;

public class livro {

    public static void main(String[] args) {
        char ch = 'x';
        System.out.println(ch);
        ch++; // incrementa o caractere, passando a valer o proximo na tabela ASCII(ou Unicode java)
        System.out.println(ch);
        ch = 80; // valor de um determinado caractere na tabela ASCII( ou unicode java)
        System.out.println(ch);

        System.out.println("10 é maior que 9: " + (10 > 9)); // precedência de operadores + > ">"

    }
}
